def validate_mbn_templates() -> None:
    """Raise ValueError if template data is malformed."""
    seen = set()
    for template in MBN_TEMPLATES:
        key = template["touchpoint_key"]
        if key in seen:
            raise ValueError(f"Duplicate MBN touchpoint_key: {key}")
        seen.add(key)

        day = template.get("day")
        if not isinstance(day, int) or day < 1:
            raise ValueError(f"{key}: day must be a positive integer")

        send_time = template.get("send_time")
        if send_time is not None:
            parts = send_time.split(":")
            if len(parts) != 2:
                raise ValueError(f"{key}: invalid send_time {send_time!r}")
            hour, minute = [int(part) for part in parts]
            if hour not in range(24) or minute not in range(60):
                raise ValueError(f"{key}: invalid send_time {send_time!r}")

        for field in ("purpose", "cta", "brief"):
            if not template.get(field):
                raise ValueError(f"{key}: {field} is required")
